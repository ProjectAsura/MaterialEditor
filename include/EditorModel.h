#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include <asdxModel.h>


///////////////////////////////////////////////////////////////////////////////
// MS_SHADER_TYPE enum
///////////////////////////////////////////////////////////////////////////////
enum MS_SHADER_TYPE
{
    // Static
    MS_SHADER_0,  // Position, Color
    MS_SHADER_1,  // Position, TangentSpace, Color, TexCoord0
    MS_SHADER_2,  // Position, TangentSpace, Color, TexCoord0, TexCoord1,
    MS_SHADER_3,  // Position, TangentSpace, Color, TexCoord0, TexCoord1, TexCoord2,
    MS_SHADER_4,  // Position, TangentSpace, Color, TexCoord0, TexCoord1, TexCoord2, TexCoord3,
};

//-----------------------------------------------------------------------------
//! @brief      メッシュシェーダを取得します.
//!
//! @param[in]      type        メッシュシェーダタイプ.
//! @return     シェーダバイトコードを返却します.
//-----------------------------------------------------------------------------
D3D12_SHADER_BYTECODE GetMeshShader(MS_SHADER_TYPE type);


///////////////////////////////////////////////////////////////////////////////
// EditorModel class
///////////////////////////////////////////////////////////////////////////////
class EditorModel
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variable.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    EditorModel();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~EditorModel();

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //!
    //! @param[in]      path        ファイルパスです.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //-------------------------------------------------------------------------
    bool Init(const char* model);

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      メッシュ数を取得します.
    //!
    //! @return     メッシュ数を返却します.
    //-------------------------------------------------------------------------
    uint32_t GetMeshCount() const;

    //-------------------------------------------------------------------------
    //! @brief      メッシュを取得します.
    //!
    //! @param[in]      index       メッシュ番号です.
    //! @return     指定されたメッシュを返却します.
    //-------------------------------------------------------------------------
    const asdx::Mesh& GetMesh(uint32_t index) const;
    
    //-------------------------------------------------------------------------
    //! @brief      バウンディングボックスを取得します.
    //!
    //! @return     バウンディングボックスを返却します.
    //-------------------------------------------------------------------------
    const asdx::BoundingBox& GetBox() const;

    //-------------------------------------------------------------------------
    //! @brief      シェーダタイプを取得します.
    //!
    //! @return     シェーダタイプを返却します.
    //-------------------------------------------------------------------------
    MS_SHADER_TYPE GetShaderType() const;

    //-------------------------------------------------------------------------
    //! @brief      ファイルパスを取得します.
    //!
    //! @return     ファイルパスを返却します.
    //-------------------------------------------------------------------------
    const std::string& GetPath() const;

    //-------------------------------------------------------------------------
    //! @brief      ワールド行列を取得します.
    //!
    //! @return     ワールド行列を返却します.
    //-------------------------------------------------------------------------
    const asdx::Matrix& GetWorld() const;

    //-------------------------------------------------------------------------
    //! @brief      スケールを設定します.
    //-------------------------------------------------------------------------
    void SetScale(const asdx::Vector3& scale);

    //-------------------------------------------------------------------------
    //! @brief      回転角を設定します(度単位).
    //-------------------------------------------------------------------------
    void SetRotate(const asdx::Vector3& rotate);

    //-------------------------------------------------------------------------
    //! @brief      平行移動量を設定します.
    //-------------------------------------------------------------------------
    void SetTranslation(const asdx::Vector3& translation);

    //-------------------------------------------------------------------------
    //! @brief      スケールを取得します.
    //-------------------------------------------------------------------------
    const asdx::Vector3& GetScale() const;

    //-------------------------------------------------------------------------
    //! @brief      回転角を取得します(度単位).
    //-------------------------------------------------------------------------
    const asdx::Vector3& GetRotate() const;

    //-------------------------------------------------------------------------
    //! @brief      平行移動量を取得します.
    //-------------------------------------------------------------------------
    const asdx::Vector3& GetTranslation() const;

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    asdx::Model         m_Model;            //!< モデル.
    std::string         m_Path;             //!< ファイルパス.
    asdx::Vector3       m_Scale;            //!< スケール.
    asdx::Vector3       m_Rotation;         //!< 回転角(度単位).
    asdx::Vector3       m_Translation;      //!< 平行移動量.
    asdx::Matrix        m_World;            //!< ワールド行列.
    MS_SHADER_TYPE      m_ShaderType;       //!< シェーダタイプ.

    //=========================================================================
    // private methods.
    //=========================================================================
    void UpdateWorld();
};
